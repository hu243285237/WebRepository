// object.hasOwnProperty(name)
// 如果这个对象包含一个名为name的属性，那返回true
// 原型链中的属性是不会被检查的
// 参数是一个字符串
var a1 = {}
a1.name = "hhhh"
console.log(a1.hasOwnProperty('name'));

