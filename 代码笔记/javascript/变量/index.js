/*
    变量分为基本数据类型和复杂数据类型

    基本数据类型：Number,String,Boolean,Undefined,Null,Symbol(ES6新添)

    复杂数据类型：Object
*/

var a = 0;
var b = '';
var c = true;
var d = undefined;
var e = null;
var f = Symbol("id");
var g = {};

console.log(typeof a);
console.log(typeof b);
console.log(typeof c);
console.log(typeof d);
console.log(typeof e);
console.log(typeof f);
console.log(typeof g);